
package org.traccar.reports.model;

public class DayNightKmSummary {

    private double daytimeKm;
    private double nighttimeKm;

    public double getDaytimeKm() {
        return daytimeKm;
    }

    public void setDaytimeKm(double daytimeKm) {
        this.daytimeKm = daytimeKm;
    }

    public double getNighttimeKm() {
        return nighttimeKm;
    }

    public void setNighttimeKm(double nighttimeKm) {
        this.nighttimeKm = nighttimeKm;
    }
}
